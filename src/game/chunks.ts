export interface BoxSpawn {
  id: number;
  x: number; // px (center)
  y: number; // px (center)
}

export interface Level {
  cols: number;
  rows: number;
  solid: Uint8Array; // rows * cols
  boxes: BoxSpawn[];
  widthPx: number;
  heightPx: number;
  goalY: number; // px — เท้าแตะระดับนี้หรือสูงกว่า = ชนะ
  spawnY: number; // px — เท้าผู้เล่นตอนเริ่ม
}

export function buildLevel(seed: number): Level {
  const rng = new Rng(seed);
  const order: string[][] = [START_CHUNK];
  let prev = -1;
  for (let i = 0; i < MIDDLE_CHUNKS; i++) {
    let pick = rng.int(MIDDLE.length);
    if (pick === prev) pick = (pick + 1 + rng.int(MIDDLE.length - 1)) % MIDDLE.length;
    prev = pick;
    order.push(MIDDLE[pick]);
  }
  order.push(GOAL_CHUNK);

  const numChunks = order.length;
  const rows = numChunks * CHUNK_H;
  const cols = CHUNK_W;
  const solid = new Uint8Array(rows * cols);
  const boxes: BoxSpawn[] = [];

  // chunk แรก (start) อยู่ล่างสุดของด่าน
  for (let i = 0; i < numChunks; i++) {
    const baseRow = rows - CHUNK_H * (i + 1);
    const grid = order[i];
    for (let r = 0; r < CHUNK_H; r++) {
      const line = grid[r];
      for (let c = 0; c < cols; c++) {
        const ch = line[c];
        const levelRow = baseRow + r;
        if (ch === '#') {
          solid[levelRow * cols + c] = 1;
        } else if (ch === 'B') {
          boxes.push({
            id: boxes.length,
            x: c * TILE + TILE / 2,
            y: levelRow * TILE + TILE / 2,
          });
        }
      }
    }
  }

  const goalRow = 0 + GOAL_ROW; // goal chunk อยู่บนสุด (baseRow = 0)
  return {
    cols,
    rows,
    solid,
    boxes,
    widthPx: cols * TILE,
    heightPx: rows * TILE,
    goalY: goalRow * TILE,
    spawnY: (rows - 2) * TILE, // ยืนบนพื้นแถว rows-2 ของ start chunk
  };
}

export function isSolid(level: Level, col: number, row: number): boolean {
  if (col < 0 || col >= level.cols) return true; // กำแพงซ้าย-ขวา
  if (row < 0) return false; // เหนือยอด = โล่ง
  if (row >= level.rows) return true; // ใต้ด่าน = ตัน (กันหลุดโลกตอนเริ่ม)
  return level.solid[row * level.cols + col] === 1;
}
